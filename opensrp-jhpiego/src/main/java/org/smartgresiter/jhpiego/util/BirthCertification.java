package org.smartgresiter.jhpiego.util;

public class BirthCertification {

    private String hasBirthCert;
    private String birthCertIssueDate;
    private String birthCertNumber;
    private String birthCertNotification;
    private String instruction;
    private String caregiverInstruction;

    public String getHasBirthCert() {
        return hasBirthCert;
    }

    public void setHasBirthCert(String hasBirthCert) {
        this.hasBirthCert = hasBirthCert;
    }

    public String getBirthCertIssueDate() {
        return birthCertIssueDate;
    }

    public void setBirthCertIssueDate(String birthCertIssueDate) {
        this.birthCertIssueDate = birthCertIssueDate;
    }

    public String getBirthCertNumber() {
        return birthCertNumber;
    }

    public void setBirthCertNumber(String birthCertNumber) {
        this.birthCertNumber = birthCertNumber;
    }

    public String getBirthCertNotification() {
        return birthCertNotification;
    }

    public void setBirthCertNotification(String birthCertNotification) {
        this.birthCertNotification = birthCertNotification;
    }

    public String getInstruction() {
        return instruction;
    }

    public void setInstruction(String instruction) {
        this.instruction = instruction;
    }

    public String getCaregiverInstruction() {
        return caregiverInstruction;
    }

    public void setCaregiverInstruction(String caregiverInstruction) {
        this.caregiverInstruction = caregiverInstruction;
    }

}
